// 0.  Функция инициализации таймера (делаем один раз при старте)
/*
 *  ┌──────┬───────────┬──────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────┐
  │ Биты │ Имя       │ Описание                                                                                                                                                                 │
  ├──────┼───────────┼──────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────┤
  │ 0    │ ENABLE    │ 1 — Счетчик включен. 0 — Выключен.                                                                                                                                       │
  │ 1    │ TICKINT   │ 1 — РАЗРЕШИТЬ ПРЕРЫВАНИЕ! Если этот бит равен 1, то когда счетчик доходит до 0, генерируется исключение (Interrupt). Если 0 — прерывание не генерируется (Polling mode). │
  │ 2    │ CLKSOURCE │ Источник частоты. 1 — Внутренняя частота процессора (AHB). 0 — Внешняя частота (AHB/8).                                                                                  │
  │ 16   │ COUNTFLAG │ (Только для чтения) Становится 1, когда счетчик дошел до 0. Сбрасывается при чтении регистра.                                                                            │
  └──────┴───────────┴──────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────────┘

 *
 */

void SysTick_Init(void) {
	 // 72 000 000 / 1000 = 72 000 тактов на 1 мс
	STK_LOAD = 72000 - 1;
    STK_VAL = 0;
    // Включаем: ENABLE, TICKINT (прерывание), CLKSOURCE (ядро)
    STK_CTRL = (1 << 0) | (1 << 1) | (1 << 2);
    }

// 1. Глобальный счетчик миллисекунд.
// volatile обязателен: main должен знать, что значение меняется "извне" (в прерывании).
volatile uint32_t ms_ticks = 0;

// 2. Обработчик прерывания SysTick.
// Процессор вызывает эту функцию автоматически каждую 1 мс.
void SysTick_Handler(void) {
	ms_ticks++;
	}

// 3. Новая функция задержки.
// Она больше не блокирует таймер, а просто ждет, пока счетчик вырастет.
void delay_ms(uint32_t ms) {
	uint32_t start = ms_ticks;
	while ((ms_ticks - start) < ms);
	}

uint32_t get_uptime_ms(void) {
	return ms_ticks;
	}


void DWT_Init(void) {
	// 1. Разрешаем использование блока DWT (бит TRCENA в регистре DEMCR)
    DEMCR |= (1 << 24);

    // 2. Обнуляем счетчик циклов
    DWT_CYCCNT = 0;

    // 3. Включаем счетчик (бит CYCCNTENA в регистре DWT_CTRL)
    DWT_CTRL |= (1 << 0);
}

void delay_us(uint32_t us) {
	// 1. Запоминаем начальное значение тактов
    uint32_t start_ticks = DWT_CYCCNT;

     // 2. Вычисляем нужное количество тактов для задержки.
     // На 72 МГц: 1 мкс = 72 такта.
     uint32_t required_ticks = us * (72000000 / 1000000);

     // 3. Ждем в пустом цикле, пока разница не достигнет нужного значения.
     // Разность (now - start) корректно обрабатывает переполнение 32-битного счетчика.
     while ((DWT_CYCCNT - start_ticks) < required_ticks);
}












